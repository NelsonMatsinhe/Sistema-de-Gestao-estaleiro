    /**
     * Seleciona o registro da tabela e preenche o formulário.
     */
    public void selectRegistryTable() {
        int selectedRow = TbUsuario.getSelectedRow();
        if (selectedRow != -1) {
            // Supondo que TbUsuario use UsuarioTableModel como modelo
            UsuarioTableModel model = (UsuarioTableModel) TbUsuario.getModel();
            Usuario usuario = model.getRowValue(selectedRow);

            txtID.setText(String.valueOf(usuario.getCodigoUsuario())); // Preenche o ID
            ftfFucionario.setText(usuario.getUserName()); // Preenche o nome de usuário
            txsenha.setText(usuario.getSenha()); // Preenche a senha
            habilitarFormulario(true); // Habilita o formulário para edição
        }
    }

    /**
     * Método para habilitar ou desabilitar o formulário.
     */
    private void habilitarFormulario(boolean ativo) {
        btNovo.setEnabled(!ativo);
        btSalvar.setEnabled(ativo && validarFormulario()); // Habilitado apenas se for válido
        btCancelar.setEnabled(ativo);
        btExcluir.setEnabled(ativo);
        txtID.setEnabled(ativo);
        txtNome.setEnabled(ativo);
        txsenha.setEnabled(ativo);
        btEditar.setEnabled(TbUsuario.getSelectedRow() >= 0 && ativo); // Habilitado se houver uma linha selecionada

        TbUsuario.setEnabled(!ativo);

        if (!ativo) {
            limpaFormulario();
        }
    }

    /**
     * Habilita o botão salvar se o formulário for válido.
     */
    private void habilitarSalvar() {
        btSalvar.setEnabled(validarFormulario());
    }
