    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (validarFormulario()) {
            if (usuario == null) {
                usuario = new Usuario();
            }
            usuario.setFuncionario((Funcionario) ftfFucionario.getValue());
            usuario.setUserName(txtNome.getText());
            usuario.setSenha(txsenha.getText());

            if (usuario.getCodigoUsuario() == 0) {
                try {
                    usuarioDAO.salvar(usuario);
                    JOptionPane.showMessageDialog(this, "Usuário salvo com sucesso.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao salvar o usuário.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            } 

            habilitarFormulario(false);
            carregarGrade();
            limpaFormulario();
        }

    }//GEN-LAST:event_btSalvarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        if (!txtID.getText().isEmpty()) {
            Long id = Long.parseLong(txtID.getText());

            int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja desativar este usuário?", "Confirmação", JOptionPane.YES_NO_OPTION);

            if (confirmacao == JOptionPane.YES_OPTION) {
                try {
                    usuarioDAO.desativarUsuario(id);  // Desativa o usuário
                    JOptionPane.showMessageDialog(this, "Usuário desativado com sucesso.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao desativar o usuário.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }

                habilitarFormulario(false);
                carregarGrade();
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um usuário para desativar.");
        }

    }//GEN-LAST:event_btExcluirActionPerformed

