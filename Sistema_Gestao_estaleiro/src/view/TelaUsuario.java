    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed
        if (validarFormulario()) {
            if (usuario == null) {
                usuario = new Usuario();
            }
            usuario.setFuncionario((Funcionario) ftfFucionario.getValue());
            usuario.setUserName(txtNome.getText());
            usuario.setSenha(txsenha.getText());
            if (rbtnAtivo.isSelected()) {
                usuario.setEstado(true);  // Ativo
            } else if (rbtnInativo.isSelected()) {
                usuario.setEstado(false); // Inativo
            }

            if (usuario.getCodigoUsuario() == 0) {
                try {
                    usuarioDAO.salvar(usuario);
                    JOptionPane.showMessageDialog(this, "Usuário salvo com sucesso.");
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao salvar o usuário.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                    return;
                }
            }

            habilitarFormulario(false);
            carregarGrade();
            limpaFormulario();
        }

    }//GEN-LAST:event_btSalvarActionPerformed

