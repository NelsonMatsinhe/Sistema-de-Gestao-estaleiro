    private void btEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btEditarActionPerformed

        if (validarFormulario()) {
            Long id;
            try {
                id = Long.parseLong(txtID.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "ID inválido.", "Erro", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Usuario usuario = usuarioDAO.buscarPorId(id);
            if (usuario != null) {
                usuario.setFuncionario((Funcionario) ftfFucionario.getValue());
                usuario.setUserName(txtNome.getText());
                usuario.setSenha(txsenha.getText());
                if (rbtnAtivo.isSelected()) {
                    usuario.setEstado(true);  // Ativo
                } else if (rbtnInativo.isSelected()) {
                    usuario.setEstado(false); // Inativo
                }
                try {
                    usuarioDAO.atualizar(usuario);
                    JOptionPane.showMessageDialog(this, "Usuário atualizado com sucesso.");
                    limpaFormulario();
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(this, "Erro ao atualizar o usuário.\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
                }

                habilitarFormulario(false);
                carregarGrade();
            }
        }


    }//GEN-LAST:event_btEditarActionPerformed

